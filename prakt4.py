def format_price(price: float) -> str:               # —Ñ—É–Ω–∫—Ü—ñ—è —Ñ–æ—Ä–º–∞—Ç—É—î —Ü—ñ–Ω—É –≤ –∫—Ä–∞—Å–∏–≤–∏–π —Ä—è–¥–æ–∫
    return f"—Ü—ñ–Ω–∞: {price:.2f} –≥—Ä–Ω"                  # –ø–æ–≤–µ—Ä—Ç–∞—î —Ü—ñ–Ω—É –∑ 2 –∑–Ω–∞–∫–∞–º–∏ –ø—ñ—Å–ª—è –∫–æ–º–∏


def check_availability(*items, stock=None) -> dict:  # –ø–µ—Ä–µ–≤—ñ—Ä—è—î –Ω–∞—è–≤–Ω—ñ—Å—Ç—å —Ç–æ–≤–∞—Ä—ñ–≤
    if stock is None:                                # —è–∫—â–æ —Å–∫–ª–∞–¥ –Ω–µ –ø–µ—Ä–µ–¥–∞–ª–∏ ‚Äî —Å—Ç–≤–æ—Ä—é—î–º–æ –ø—É—Å—Ç–∏–π
        stock = {}
    return {item: stock.get(item, False) for item in items}  # –ø–æ–≤–µ—Ä—Ç–∞—î–º–æ —Å–ª–æ–≤–Ω–∏–∫ —Ç–æ–≤–∞—Ä: –Ω–∞—è–≤–Ω—ñ—Å—Ç—å


def process_order(order: list, stock: dict, prices: dict):   # –æ–ø—Ä–∞—Ü—å–æ–≤—É—î –∑–∞–º–æ–≤–ª–µ–Ω–Ω—è
    availability = check_availability(*order, stock=stock)    # –ø–µ—Ä–µ–≤—ñ—Ä—è—î–º–æ –≤—Å—ñ —Ç–æ–≤–∞—Ä–∏ –≤ –∑–∞–º–æ–≤–ª–µ–Ω–Ω—ñ

    if not all(availability.values()):                        # —è–∫—â–æ —Ö–æ—á –æ–¥–∏–Ω —Ç–æ–≤–∞—Ä –≤—ñ–¥—Å—É—Ç–Ω—ñ–π
        print("‚ùå –ó–∞–º–æ–≤–ª–µ–Ω–Ω—è –Ω–µ–º–æ–∂–ª–∏–≤–µ, –±–æ –¥–µ—è–∫—ñ —Ç–æ–≤–∞—Ä–∏ –≤—ñ–¥—Å—É—Ç–Ω—ñ:")  
        for item, available in availability.items():          # –ø–µ—Ä–µ–±–∏—Ä–∞—î–º–æ –≤—Å—ñ —Ç–æ–≤–∞—Ä–∏
            if not available:                                 # —è–∫—â–æ —Ç–æ–≤–∞—Ä –≤—ñ–¥—Å—É—Ç–Ω—ñ–π
                print(f"   - {item}")                         # –ø–æ–∫–∞–∑—É—î–º–æ —è–∫–∏–π
        return                                                # –ø—Ä–∏–ø–∏–Ω—è—î–º–æ —Ä–æ–±–æ—Ç—É —Ñ—É–Ω–∫—Ü—ñ—ó

    total = sum(prices[item] for item in order)               # —Ä–∞—Ö—É—î–º–æ –∑–∞–≥–∞–ª—å–Ω—É —Å—É–º—É –∑–∞–º–æ–≤–ª–µ–Ω–Ω—è
    print(" –í–∞—à–µ –∑–∞–º–æ–≤–ª–µ–Ω–Ω—è:")
    for item in order:                                        # –≤–∏–≤–æ–¥–∏–º–æ –∫–æ–∂–µ–Ω —Ç–æ–≤–∞—Ä
        print(f"   - {item}: {format_price(prices[item])}")   # –ø–æ–∫–∞–∑—É—î–º–æ —Ü—ñ–Ω—É
    print(f" –ó–∞–≥–∞–ª—å–Ω–∞ {format_price(total)}")                 # –∑–∞–≥–∞–ª—å–Ω–∞ —Å—É–º–∞


def main():                                                   # –≥–æ–ª–æ–≤–Ω–∞ —Ñ—É–Ω–∫—Ü—ñ—è –ø—Ä–æ–≥—Ä–∞–º–∏
    prices = {                                                # —Å–ª–æ–≤–Ω–∏–∫ —Ü—ñ–Ω —Ç–æ–≤–∞—Ä—ñ–≤
        "—Ö–ª—ñ–±": 25.5,
        "–º–æ–ª–æ–∫–æ": 32.0,
        "—è–π—Ü—è": 48.75,
        "—Å–∏—Ä": 120.0,
        "–º–∞—Å–ª–æ": 85.3
    }

    stock = {                                                 # —Å–ª–æ–≤–Ω–∏–∫ –Ω–∞—è–≤–Ω–æ—Å—Ç—ñ —Ç–æ–≤–∞—Ä—ñ–≤
        "—Ö–ª—ñ–±": True,
        "–º–æ–ª–æ–∫–æ": True,
        "—è–π—Ü—è": False,                                        # —è—î—Ü—å –Ω–µ–º–∞—î
        "—Å–∏—Ä": True,
        "–º–∞—Å–ª–æ": True
    }

    while True:                                               # –Ω–µ—Å–∫—ñ–Ω—á–µ–Ω–Ω–µ –º–µ–Ω—é
        print("\n--- –ú–∞–≥–∞–∑–∏–Ω ---")
        print("1. –ü–µ—Ä–µ–≥–ª—è–Ω—É—Ç–∏ —Ü—ñ–Ω—É —Ç–æ–≤–∞—Ä—É")                   # –ø—É–Ω–∫—Ç –º–µ–Ω—é ‚Äî –ø–æ–¥–∏–≤–∏—Ç–∏—Å—è —Ü—ñ–Ω—É
        print("2. –ó—Ä–æ–±–∏—Ç–∏ –∑–∞–º–æ–≤–ª–µ–Ω–Ω—è")                        # –ø—É–Ω–∫—Ç –º–µ–Ω—é ‚Äî –∑–∞–º–æ–≤–ª–µ–Ω–Ω—è
        print("3. –í–∏–π—Ç–∏")                                     # –≤–∏—Ö—ñ–¥ –∑ –ø—Ä–æ–≥—Ä–∞–º–∏

        choice = input("–í–∏–±–µ—Ä—ñ—Ç—å –æ–ø—Ü—ñ—é: ")                    # –∫–æ—Ä–∏—Å—Ç—É–≤–∞—á –≤–≤–æ–¥–∏—Ç—å –≤–∏–±—ñ—Ä

        if choice == "1":                                     # —è–∫—â–æ –≤–∏–±—Ä–∞–ª–∏ –ø–µ—Ä–µ–≥–ª—è–¥ —Ü—ñ–Ω–∏
            item = input("–í–≤–µ–¥—ñ—Ç—å –Ω–∞–∑–≤—É —Ç–æ–≤–∞—Ä—É: ").strip().lower()  # –≤–≤–æ–¥–∏–º–æ –Ω–∞–∑–≤—É
            if item in prices:                                # –ø–µ—Ä–µ–≤—ñ—Ä—è—î–º–æ —á–∏ —Ç–æ–≤–∞—Ä —ñ—Å–Ω—É—î
                print(f"{item}: {format_price(prices[item])}")       # –ø–æ–∫–∞–∑—É—î–º–æ —Ü—ñ–Ω—É
            else:
                print("‚ùå –¢–∞–∫–æ–≥–æ —Ç–æ–≤–∞—Ä—É –Ω–µ–º–∞—î.")              # —Ç–æ–≤–∞—Ä –Ω–µ –∑–Ω–∞–π–¥–µ–Ω–æ

        elif choice == "2":                                   # —è–∫—â–æ –æ–±—Ä–∞–ª–∏ –∑–∞–º–æ–≤–ª–µ–Ω–Ω—è
            order = input("–í–≤–µ–¥—ñ—Ç—å —Ç–æ–≤–∞—Ä–∏ —á–µ—Ä–µ–∑ –∫–æ–º—É: ")      # –≤–≤–æ–¥–∏–º–æ —Å–ø–∏—Å–æ–∫ —Ç–æ–≤–∞—Ä—ñ–≤
            order = order.strip().lower().split(",")          # —Ä–æ–∑–¥—ñ–ª—è—î–º–æ –ø–æ –∫–æ–º–∞—Ö
            order = [item.strip() for item in order]          # –∑–∞–±–∏—Ä–∞—î–º–æ –∑–∞–π–≤—ñ –ø—Ä–æ–±—ñ–ª–∏
            process_order(order, stock, prices)               # –æ–±—Ä–æ–±–ª—è—î–º–æ –∑–∞–º–æ–≤–ª–µ–Ω–Ω—è

        elif choice == "3":                                   # –≤–∏—Ö—ñ–¥ –∑ –ø—Ä–æ–≥—Ä–∞–º–∏
            print("üëã –î—è–∫—É—î–º–æ –∑–∞ –≤—ñ–¥–≤—ñ–¥—É–≤–∞–Ω–Ω—è!")
            break

        else:
            print("‚ùå –ù–µ–≤—ñ—Ä–Ω–∏–π –≤–∏–±—ñ—Ä!")                       # –Ω–µ–ø—Ä–∞–≤–∏–ª—å–Ω–∏–π –ø—É–Ω–∫—Ç –º–µ–Ω—é


if __name__ == "__main__":                                   # –∑–∞–ø—É—Å–∫–∞—î–º–æ main —Ç—ñ–ª—å–∫–∏ —è–∫—â–æ —Ñ–∞–π–ª –∑–∞–ø—É—Å–∫–∞—î—Ç—å—Å—è –Ω–∞–ø—Ä—è–º—É
    main()                                                    # –≤–∏–∫–ª–∏–∫–∞—î–º–æ –≥–æ–ª–æ–≤–Ω—É —Ñ—É–Ω–∫—Ü—ñ—é
